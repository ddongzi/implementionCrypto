void key_expension(key_t key,expanded_key_t expandedkey)
{
    int i,j,ri;
    uint8_t w[4];   //prev cur
    uint8_t rcon[4]={'\x00'};
    for(i=0;i<4;i++){
        for (j=0;j<NK;j++){
            expandedkey[i][j]=key[i][j];
        }
    }

    for (j=NK;j<NB*(NR+1);j++){


        for(i=0;i<4;i++){
            w[i]=expandedkey[i][j-1];
        }

        if (NK>6&&j%NK==4){
            word_bytesub(w);
        }

        if (j%NK==0){
            rcon[0]=RC[j/NK-1];
            word_rotbyte(w);
            word_bytesub(w);
            word_xor(w,rcon,w);
        }

        for(i=0;i<4;i++){
            expandedkey[i][j]=w[i]^expandedkey[i][j-NK];
        }

    }

}
void word_xor(uint8_t a[4],uint8_t b[4],uint8_t c[4])
{
    int i;
    for (i=0;i<4;i++){
        c[i]=a[i]^b[i];
    }
}
void word_rotbyte(uint8_t a[4])
{
    int i;
    uint8_t tmp=a[0];
    for (i=0;i<3;i++){
        a[i]=a[i+1];
    }
    a[i]=tmp;
}
void word_bytesub(uint8_t a[4])
{
    int i;
    for (i  = 0; i <4 ;i ++) {
        a[i]=sbox[a[i]];
    }
}

void addroundkey(state_t state,round_key_t roundkey)
{
    int i,j;
    for (i=0;i<4;i++){
        for ( j = 0; j < NB; j++) {
            state[i][j]=state[i][j]^roundkey[i][j];
        }
    }
}
void get_round_key(const expanded_key_t expandedKey,unsigned int r,round_key_t roundKey)
{
    int i,j;
    for (i=0;i<4;i++){
        for (j=r*NB;j<NB*(r+1);j++){
            roundKey[i][j-r*NB]=expandedKey[i][j];
        }
    }
}
void roundfunc(state_t state,round_key_t roundKey)
{
    bytesub(state);
    shiftrow(state);
    mixcolumn(state);
    addroundkey(state,roundKey);
}
void finalround(state_t state,round_key_t roundKey)
{
    bytesub(state);
    shiftrow(state);
    addroundkey(state,roundKey);
}
void bytesub(state_t state)
{
    int i,j;
    for(i=0;i<4;i++){
        for (j=0;j<NB;j++){
            state[i][j]=sbox[state[i][j]];
        }
    }
}
void shiftrow(state_t state)
{
    int i,j,num;
    uint8_t *tmp;
    for(i=1;i<4;i++){
        num=C[i-1];
        tmp=(uint8_t *)malloc(num* sizeof(uint8_t));
        for (j=0;j<num;j++){
            tmp[j]=state[i][j];
        }
        for (;j<NB;j++){
            state[i][j-num]=state[i][j];
        }
        for (j=0;j<num;j++){
            state[i][NB-1-j]=tmp[num-1-j];
        }
        free(tmp);
    }
}
void mixcolumn(state_t state)
{
    uint8_t *p;
    uint8_t c[4];
    uint8_t a[4];
    int j,l;
    for (j=0;j<NB;j++){
        p=&state[0][j];
        for (l=0;l<4;l++){
            a[l]=*(p+l*NB);
        }
        c[0]=gmul(0x02,a[0])^gmul(0x03,a[1])^gmul(0x01,a[2])^gmul(0x01,a[3]);
        c[1]=gmul(0x01,a[0])^gmul(0x02,a[1])^gmul(0x03,a[2])^gmul(0x01,a[3]);
        c[2]=gmul(0x01,a[0])^gmul(0x01,a[1])^gmul(0x02,a[2])^gmul(0x03,a[3]);
        c[3]=gmul(0x03,a[0])^gmul(0x01,a[1])^gmul(0x01,a[2])^gmul(0x02,a[3]);
        for (l=0;l<4;l++){
            *(p+l*NB)=c[l];
        }
    }
}

uint8_t gadd(uint8_t a,uint8_t b)
{
    return a^b;
}
uint8_t gmul(uint8_t a,uint8_t b)
{
    uint8_t p=0;
    while (a!=0&&b!=0){
        if (b&1){
            p^=a;
        }
        if (a&0x80){
            a=(a<<1)^0x11b;
        } else
            a<<=1;
        b>>=1;
    }
    return p;
}



















