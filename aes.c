const uint8_t C[3]={0x01,0x02,0x03};
const uint8_t RC[10]={0x01 ,0x02,0x04,0x08,0x10 ,0x20,0x40 ,0x80,0x1B ,0x36};

void state_to_str(state_t state,unsigned char *s)
{
    int i,j,k=0;
    for (j=0;j<NB;j++){
        for (i=0;i<4;i++,k++){
            s[k]=state[i][j];
        }
    }
}
void str_to_state(unsigned char *s,state_t state)
{
    int k=0;
    while (k<4*NB){
        state[k%NB][k/NB]=s[k];
        k++;
    }
}
void key_to_state(unsigned char *s,key_t key)
{
    int k=0;
    while (k<4*NK){
        key[k%NK][k/NK]=s[k];
        k++;
    }
}

